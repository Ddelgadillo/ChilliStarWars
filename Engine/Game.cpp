void Game::Go()
{
	gfx.BeginFrame();	
	UpdateModel();
	ComposeFrame();
	gfx.EndFrame();
}

void Game::UpdateModel()
{
	ScreenBoundary(mandoPosX, mandoPosY, mandoWidth, mandoHeight);
	MoveMando(mandoPosX, mandoPosY);

	if (BoxCollision(mandoPosX, mandoPosY, blasterPosX, blasterPosY, mandoWidth, mandoHeight, blasterWidth, blasterHeight))
	{
		isBlasterCollected = true;
	}
}

