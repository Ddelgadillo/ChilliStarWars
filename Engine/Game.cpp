void Game::ScreenBoundary(float& x, float& y, float width, float height)
{
	if (x + width >= gfx.ScreenWidth)
	{
		x = gfx.ScreenWidth - width;
	}
	else if (x < 0)
	{
		x = 0;
	}

	if (y + height >= gfx.ScreenHeight)
	{
		y = (gfx.ScreenHeight - 1) - height;
	}
	else if (y < 0)
	{
		y = 0;
	}
}

void Game::MoveMando(float& x, float& y)
{
	if (wnd.kbd.KeyIsPressed(VK_UP))
	{
		y--;
	}
	else if (wnd.kbd.KeyIsPressed(VK_DOWN))
	{
		y++;
	}

	if (wnd.kbd.KeyIsPressed(VK_LEFT))
	{
		x--;
	}
	else if (wnd.kbd.KeyIsPressed(VK_RIGHT))
	{
		x++;
	}
}

bool Game::BoxCollision(float& x1, float& y1, float& x2, float& y2, float width1, float height1, float width2, float height2)
{
	return x1 + width1 >= x2 && x1 <= x2 + width2
		&& y1 + height1 >= y2 && y1 <= y2 + height2;
}

void Game::ComposeFrame()
{
	DrawMando(mandoPosX, mandoPosY, mandoWidth, mandoHeight);

	if (!isBlasterCollected)
	{
		DrawBlaster(blasterPosX, blasterPosY, blasterWidth, blasterHeight);
	}
}
